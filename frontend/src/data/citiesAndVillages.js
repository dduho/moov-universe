// Fonction utilitaire pour rechercher des villes/villages
export function searchCities(region, prefecture, commune, query = '') {
  if (!region || !prefecture || !commune) return [];
  
  const cities = citiesAndVillages[region]?.[prefecture]?.[commune] || [];
  
  if (!query) return cities;
  
  const searchTerm = query.toLowerCase().trim();
  return cities.filter(city => 
    city.toLowerCase().includes(searchTerm)
  );
}

// Fonction pour obtenir toutes les villes d'une commune
export function getCitiesByCommune(region, prefecture, commune) {
  return citiesAndVillages[region]?.[prefecture]?.[commune] || [];
}
