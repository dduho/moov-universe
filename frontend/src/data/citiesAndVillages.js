export function searchCities(region, prefecture, commune, query = "") {
  if (!region || !prefecture || !commune) return [];

  const cities = citiesAndVillages[region]?.[prefecture]?.[commune] || [];

  if (!query) return cities;

  const searchTerm = query.toLowerCase().trim();
  return cities.filter((city) => city.toLowerCase().includes(searchTerm));
}

export function getCitiesByCommune(region, prefecture, commune) {
  return citiesAndVillages[region]?.[prefecture]?.[commune] || [];
}

// Cantons = clés de commune (ex: Blitta_1). Pratique pour l'autocomplete.
export function getCantonsByPrefecture(region, prefecture) {
  return Object.keys(citiesAndVillages[region]?.[prefecture] || {});
}

const normalize = (val = "") =>
  String(val || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();

// Suggère cantons + villes/villages dans la préfecture donnée.
export function searchCitiesAndCantons(region, prefecture, commune, query = "", limit = 50) {
  if (!region || !prefecture) return [];

  const cantonKeys = getCantonsByPrefecture(region, prefecture);
  const allCities = commune
    ? getCitiesByCommune(region, prefecture, commune)
    : cantonKeys.flatMap((c) => getCitiesByCommune(region, prefecture, c));

  const seen = new Set();
  const term = normalize(query);

  const push = (label, type) => {
    const key = `${type}:${normalize(label)}`;
    if (seen.has(key)) return;
    seen.add(key);
    results.push({ label, type });
  };

  const results = [];

  // Cantons d'abord
  cantonKeys.forEach((c) => {
    if (!term || normalize(c).includes(term)) {
      push(c, "canton");
    }
  });

  // Puis les villes/quartiers
  allCities.forEach((city) => {
    if (!term || normalize(city).includes(term)) {
      push(city, "locality");
    }
  });

  return results.slice(0, limit);
}
