/**
 * Vrifie si un point est dans une bounding box
 */
function isPointInBounds(lat, lng, bounds) {
  return lat >= bounds.minLat && 
         lat <= bounds.maxLat && 
         lng >= bounds.minLng && 
         lng <= bounds.maxLng;
}

/**
 * Algorithme Ray Casting pour vrifier si un point est dans un polygone
 */
function isPointInPolygon(lat, lng, polygon) {
  let inside = false;
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const xi = polygon[i][0], yi = polygon[i][1];
    const xj = polygon[j][0], yj = polygon[j][1];
    
    const intersect = ((yi > lng) !== (yj > lng)) &&
      (lat < (xj - xi) * (lng - yi) / (yj - yi) + xi);
    if (intersect) inside = !inside;
  }
  return inside;
}

/**
 * Dtermine la rgion relle d'un point GPS
 */
function getRegionFromCoordinates(lat, lng) {
  // Vrifier d'abord le Grand Lom (zone spciale dans Maritime)
  if (regionBoundaries.LOME) {
    const lome = regionBoundaries.LOME;
    if (isPointInBounds(lat, lng, lome.bounds) &&
        isPointInPolygon(lat, lng, lome.polygon)) {
      return {
        region: 'MARITIME',
        name: 'Grand Lom (Maritime)'
      };
    }
  }

  // Vrifier les autres rgions
  for (const [regionCode, regionData] of Object.entries(regionBoundaries)) {
    if (regionCode === 'LOME') continue; // Dj vrifi

    // Optimisation: vrifier d'abord la bounding box
    if (!isPointInBounds(lat, lng, regionData.bounds)) {
      continue;
    }

    // Vrifier le polygone
    if (isPointInPolygon(lat, lng, regionData.polygon)) {
      return {
        region: regionCode,
        name: regionData.name
      };
    }
  }

  return null;
}

/**
 * Valide si les coordonnes GPS correspondent  la rgion dclare
 */
export function validateRegionCoordinates(lat, lng, declaredRegion) {
  if (!lat || !lng || !declaredRegion) {
    return {
      is_valid: true,
      has_alert: false,
      message: null
    };
  }

  const actualRegion = getRegionFromCoordinates(lat, lng);

  // Point hors du Togo
  if (!actualRegion) {
    return {
      is_valid: false,
      has_alert: true,
      alert_type: 'error',
      message: 'Les coordonnes GPS semblent tre situes en dehors du Togo',
      declared_region: declaredRegion,
      actual_region: null
    };
  }

  // Comparer les rgions
  const normalizedDeclared = declaredRegion.toUpperCase();
  const normalizedActual = actualRegion.region.toUpperCase();

  if (normalizedDeclared !== normalizedActual) {
    return {
      is_valid: false,
      has_alert: true,
      alert_type: 'warning',
      message: `Incohrence gographique : le PDV est dclar dans la rgion "${declaredRegion}" mais les coordonnes GPS indiquent la rgion "${actualRegion.name}"`,
      declared_region: declaredRegion,
      actual_region: actualRegion.region,
      actual_region_name: actualRegion.name
    };
  }

  return {
    is_valid: true,
    has_alert: false,
    declared_region: declaredRegion,
    actual_region: actualRegion.region,
    actual_region_name: actualRegion.name
  };
}
